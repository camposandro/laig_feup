/**
 * MyColor class, representing a rgba color.
 */
class MyColor {

	/**
     * @constructor
     * @param {r} r red value
     * @param {g} g green value
     * @param {b} r blue value
     * @param {a} a alpha value
     */
    constructor(r, g, b, a) {
        this.r = r;
        this.g = g;
        this.b = b;
        this.a = a;
    }
}