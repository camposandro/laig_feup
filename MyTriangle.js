/**
 * MyTriangle class, representing a triangle.
 */
class MyTriangle  extends MyEntity{

	/**
     * @constructor
     * @param {id,p1,p2,p3,}
     */
    constructor(id,x1,y1,z1,x2,y2,z2,x2,y2,z2) {
        super(id);
       this.p1 = [x1,y1,z1];
       this.p2 = [x2,y2,z2];
       this.p2 = [x2,y2,z2];
       this.p3 = [x3,y3,z3];
    }
}